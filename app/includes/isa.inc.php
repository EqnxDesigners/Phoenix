<section class="row sub-page">
    <h3>La gestion académique, un domaine en soi</h3>
    <div class="row">
        <p class="text-justify">
            La gestion académique des universités, des hautes écoles ou des écoles publiques (primaires, secondaires, supérieures, professionnelles) connaît au fil des années de nombreuses évolutions, notamment au niveau des formations et des réglements d'études. Ce domaine porte aussi sur une très large couverture fonctionnelle et pourtant concerne tous les secteurs organisationnels d’une école.
        </p>
        <p class="text-justify">
            Le logiciel IS-Academia a été spécialement conçu sur cette base. Il est le fruit d’un cahier des charges qui a été établi par plus de 30 hautes écoles et avec une participation très active de l’école polytechnique fédérale de Lausanne.
        </p>
        <p class="text-justify">
            IS-Academia est un logiciel multilingue, multi-école et entièrement paramétrable. Il offre également des possibilités d’interface avec les grands ERP du marché ou tout autre système d’information et fonctionne intégralement en mode Intranet, Extranet et Internet.
        </p>
        <p class="text-justify">
            IS-Academia gère tous les processus liés au parcours académique de vos étudiants. Il couvre ainsi tous les types d’études (universitaire, HES, ES, primaire et secondaire) et tous les niveaux d’enseignement (formation de base, bachelor, master/minor, formation modulaire, post-formation, formation continue et doctorat). Les besoins liés aux accords de Bologne et aux crédits ECTS, sont bien évidemment également entièrement couverts.
        </p>
    </div>
</section>
